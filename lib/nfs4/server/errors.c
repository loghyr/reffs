bool nfs4_error_valid_for_op(enum nfs_opnum4 op, enum nfsstat4 stat)
{
	size_t i, j;

	for (i = 0; op_errors[i].ne_op; i++)
		if (op_errors[i].ne_op == op)
			for (j = 0; op_errors[i].ne_allowed[j] != NFS4_OK; j++)
				if (op_errors[i].ne_allowed[j] == stat)
					return true;

	return false;
}

bool nfs4_error_valid_for_cb_op(enum nfs_cb_opnum4 op, enum nfsstat4 stat)
{
	size_t i, j;

	for (i = 0; cb_op_errors[i].ne_op; i++)
		if (cb_op_errors[i].ne_op == op)
			for (j = 0; cb_op_errors[i].ne_allowed[j] != NFS4_OK;
			     j++)
				if (cb_op_errors[i].ne_allowed[j] == stat)
					return true;

	return false;
}

nfsstat4 errno_to_nfs4(int error, nfs_opnum4 op)
{
	nfsstat4 status;

	if (error == 0)
		return NFS4_OK;

	/*
	 * Means it is the compound processing before
	 * we got to an op.
	 */
	if (op < OP_ACCESS) {
		return NFS4ERR_SERVERFAULT;
	}

	if (error < 0)
		error = -error;

	switch (error) {
	case EPERM:
		status = NFS4ERR_PERM;
		break;
	case ENOENT:
		status = NFS4ERR_NOENT;
		break;
	case EIO:
		status = NFS4ERR_IO;
		break;
	case ENXIO:
		status = NFS4ERR_NXIO;
		break;
	case EACCES:
		status = NFS4ERR_ACCESS;
		break;
	case EEXIST:
		status = NFS4ERR_EXIST;
		break;
	case EXDEV:
		status = NFS4ERR_XDEV;
		break;
	case ENODEV:
		status = NFS4ERR_SERVERFAULT;
		break;
	case ENOTDIR:
		status = NFS4ERR_NOTDIR;
		break;
	case EISDIR:
		status = NFS4ERR_ISDIR;
		break;
	case EINVAL:
		status = NFS4ERR_INVAL;
		break;
	case EFBIG:
		status = NFS4ERR_FBIG;
		break;
	case ENOSPC:
		status = NFS4ERR_NOSPC;
		break;
	case EROFS:
		status = NFS4ERR_ROFS;
		break;
	case EMLINK:
		status = NFS4ERR_MLINK;
		break;
	case ENAMETOOLONG:
		status = NFS4ERR_NAMETOOLONG;
		break;
	case ENOTEMPTY:
		status = NFS4ERR_NOTEMPTY;
		break;
	case EDQUOT:
		status = NFS4ERR_DQUOT;
		break;
	case ESTALE:
		status = NFS4ERR_STALE;
		break;
	case EBADHANDLE:
		status = NFS4ERR_BADHANDLE;
		break;
	case ENOTSYNC:
		status = NFS4ERR_SERVERFAULT;
		break;
	case EBADTYPE:
		status = NFS4ERR_BADTYPE;
		break;
	case EJUKEBOX:
		status = NFS4ERR_DELAY;
		break;
	case EBADNAME:
		status = NFS4ERR_BADNAME;
		break;
	case EWRONGTYPE:
		status = NFS4ERR_WRONG_TYPE;
		break;
	case EMSGSTARTED:
		status = NFS4ERR_NOT_SAME;
		break;
	case EREADDIRFULL:
		status = NFS4ERR_TOOSMALL;
		break;
	case ENAMETOOSMALL:
		status = NFS4ERR_NAMETOOLONG;
		break;
	case EBADXDR:
		status = NFS4ERR_BADXDR;
		break;
	case EATTRNOTSUP:
		status = NFS4ERR_ATTRNOTSUPP;
		break;
	case EBADOWNER:
		status = NFS4ERR_BADOWNER;
		break;
	default:
		status = NFS4ERR_SERVERFAULT;
		break;
	}

	/*
	 * Look op if it is a valid error for the op
	 * and if not, set it to NFS4ERR_SERVERFAULT!
	 */
	if (!nfs4_error_valid_for_op(op, status)) {
		LOG("Error code %d for operation %d mapped to %d and became %d",
		    error, op, status, NFS4ERR_SERVERFAULT);
		status = NFS4ERR_SERVERFAULT;
	}

	return status;
}
